package org.ballcat.fieldcrypt.mybatisplus.mapper;

import com.baomidou.mybatisplus.core.conditions.query.LambdaQueryWrapper;
import com.baomidou.mybatisplus.core.mapper.BaseMapper;
import com.baomidou.mybatisplus.core.toolkit.Wrappers;
import org.ballcat.fieldcrypt.mybatisplus.model.User;

/**
 * 测试用 Mapper
 */
public interface UserMapper extends BaseMapper<User> {

	default User selectByMobile(User user) {
		LambdaQueryWrapper<User> wrapper = Wrappers.lambdaQuery(User.class).eq(User::getMobile, user.getMobile());
		return selectOne(wrapper);
	}

}
